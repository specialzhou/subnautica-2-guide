function replaceWithToken(output, source, token) {
  const escaped = source.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const left = /^[A-Za-z]/.test(source) ? "(?<![A-Za-z])" : "";
  const right = /[A-Za-z0-9]$/.test(source) ? "(?![A-Za-z0-9])" : "";
  return output.replace(new RegExp(`${left}${escaped}${right}`, "g"), token);
}

function translate(html, dictionary, locale) {
  const attributes = [];
  let protectedHtml = html.replace(/\b(?:href|src)="[^"]*"|\bcontent="https?:\/\/[^"]*"/g, (attribute) => {
    const token = `__URL_ATTRIBUTE_${attributes.length}__`;
    attributes.push(attribute);
    return token;
  });
  const dictionaryValues = [];
  const dictionaryEntries = Object.entries(dictionary).sort(([a], [b]) => b.length - a.length);
  const protectDictionaryEntry = ([source, target]) => {
    const token = `__DT_${dictionaryValues.length}__`;
    dictionaryValues.push(target);
    protectedHtml = replaceWithToken(protectedHtml, source, token);
  };
  // Protect complete phrases first, then proper names, then single-word UI terms.
  // This prevents `Basic Fins` becoming `Basic 脚蹬` while still allowing a
  // full sentence containing a game name to match as one translation unit.
  for (const entry of dictionaryEntries.filter(([source]) => /\s|[.,;:!?]/.test(source))) {
    if (localizedNames[entry[0]]?.[locale]) continue;
    protectDictionaryEntry(entry);
  }
  const names = [];
  for (const [source, translations] of Object.entries(localizedNames).sort(([a], [b]) => b.length - a.length)) {
    const localized = translations[locale];
    if (!localized) continue;
    const token = `__LN_${names.length}__`;
    names.push(locale === "zh-cn" ? `${localized}（${source}）` : `${localized} (${source})`);
    protectedHtml = replaceWithToken(protectedHtml, source, token);
  }
  for (const entry of dictionaryEntries.filter(([source]) => !/\s|[.,;:!?]/.test(source))) {
    if (localizedNames[entry[0]]?.[locale]) continue;
    protectDictionaryEntry(entry);
  }
  const withNames = names.reduce((output, name, index) => output.replaceAll(`__LN_${index}__`, name), protectedHtml);
  const withDictionary = dictionaryValues.reduce((output, value, index) => output.replaceAll(`__DT_${index}__`, value), withNames);
  return attributes.reduce((output, attribute, index) => output.replace(`__URL_ATTRIBUTE_${index}__`, attribute), withDictionary);
}

function stripLocaleMetadata(html) {
  return html
    .replace(/<link rel="alternate" hreflang="[^"]+" href="[^"]+">/g, "")
    .replace(/<link rel="stylesheet" href="\/subnautica-2-guide\/locale\.css">/g, "")
    .replace(/<link rel="stylesheet" href="\/subnautica-2-guide\/(?:en\/|zh-cn\/|ru\/)?search\.css(?:\?v=\d+)?">/g, "")
    .replace(/<script defer src="\/subnautica-2-guide\/(?:en\/|zh-cn\/|ru\/)?search\.js(?:\?v=\d+)?"><\/script>/g, "")
    .replace(/<button class="global-search-trigger"[^>]*>.*?<\/button>/g, "")
    .replace(/<(figure|div) class="record-media[^"]*"[^>]*>.*?<\/\1>/gs, "")
    .replace(/<span class="language-switcher"[^>]*>.*?<\/span>/g, "");
}

function localPath(pagePath, locale) {
  return `${pathBase}${locale}/${pagePath === "index.html" ? "" : pagePath}`;
}

function decorate(sourceHtml, pagePath, locale) {
  const config = locales[locale];
  let html = stripLocaleMetadata(sourceHtml).replace(/<html lang="[^"]+">/, `<html lang="${config.htmlLang}">`);
  if (locale !== "en") html = translate(html, config.dictionary, locale);
  html = html.replaceAll(pathBase, `${pathBase}${locale}/`)
    .replaceAll(`${pathBase}${locale}/styles.css`, `${pathBase}styles.css`)
    .replaceAll(`${pathBase}${locale}/guide.css`, `${pathBase}guide.css`)
    .replaceAll(`${pathBase}${locale}/favicon.svg`, `${pathBase}favicon.svg`)
    .replaceAll(`${pathBase}${locale}/app.js`, `${pathBase}app.js`)
    .replaceAll(`${pathBase}${locale}/data/`, `${pathBase}data/`)
    .replaceAll('href="favicon.svg"', `href="${pathBase}favicon.svg"`)
    .replaceAll('href="styles.css"', `href="${pathBase}styles.css"`)
    .replaceAll('href="guide.css"', `href="${pathBase}guide.css"`)
    .replaceAll('src="app.js"', `src="${pathBase}app.js"`)
    .replaceAll('href="data/', `href="${pathBase}data/`)
    .replaceAll('fetch("data/', `fetch("${pathBase}data/`);
  html = html.replace(/<link rel="canonical" href="[^"]+">/, `<link rel="canonical" href="${siteBase}${locale}/${pagePath === "index.html" ? "" : pagePath}">`);
  const alternates = [`<link rel="alternate" hreflang="x-default" href="${siteBase}${pagePath === "index.html" ? "" : pagePath}">`, ...Object.keys(locales).map((code) => `<link rel="alternate" hreflang="${locales[code].htmlLang}" href="${siteBase}${code}/${pagePath === "index.html" ? "" : pagePath}">`)].join("");
  html = html.replace("</head>", `<link rel="stylesheet" href="${pathBase}locale.css">${alternates}</head>`);
  const switcher = `<span class="language-switcher" aria-label="${config.dictionary.Language ?? "Language"}">${Object.entries(locales).map(([code, value]) => `<a href="${localPath(pagePath, code)}"${code === locale ? ' aria-current="page"' : ""}>${value.label}</a>`).join("")}</span>`;
  html = html.replace("</nav>", `${switcher}</nav>`);
  return html;
}

